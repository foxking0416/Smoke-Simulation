#include "Bubbles.h"


////////////////////////////////////////////////////
// default constructor
////////////////////////////////////////////////////
Bubbles::Bubbles()
{
}


////////////////////////////////////////////////////
// copy constructor
////////////////////////////////////////////////////
Bubbles::Bubbles( const Bubbles& orig )
{
   m_positions = orig.m_positions;
}


////////////////////////////////////////////////////
// destructor
////////////////////////////////////////////////////
Bubbles::~Bubbles()
{
}


////////////////////////////////////////////////////
// assignment operator
////////////////////////////////////////////////////
Bubbles& Bubbles::operator=( const Bubbles& orig )
{
   if ( this == &orig ) {
      return *this;
   }

   m_positions = orig.m_positions;
   return *this;
}